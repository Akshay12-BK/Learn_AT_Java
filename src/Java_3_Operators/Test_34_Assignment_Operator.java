package Java_3_Operators;

public class Test_34_Assignment_Operator {
    public static void main(String[] args) {

        // Arithmetic operator- +,-,*,/,%
        int a= 10;
        int b= 5;
        float c = 3.1f;
        System.out.println(a+b);
        System.out.println(a-b);
        System.out.println(a*b);
        System.out.println(a/b);
        System.out.println(a%b);
        System.out.println("=======================");
        System.out.println(a+c);
        System.out.println(a-c);
        System.out.println("a*c");

    }
}
