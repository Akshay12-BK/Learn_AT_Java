package Java_3_Operators;

public class Test_37_Relational_Operator {
    public static void main(String[] args) {
        /*
        < Less than  & > Greater than
        <= Less than equal to  >= Greater than equal to
        == Equal to
        != Not Equal to
        All OF THEM WILL RESULT BOOLEAN OUTPUT
         */
        int a = 10;
        int b = 20;
        boolean c = a>b;
        System.out.println(c);

        int age_Dudu = 27;
        int age_bubu = 24;
        boolean result = age_Dudu >= age_bubu;
        System.out.println(result);
    }
}
