package Java_3_Operators;

public class Test_47_Interview_Equal_And_Equal_to {
    public static void main(String[] args) {
        int a= 10;
        int b= 20;
        int c= 20;

        System.out.println(a);  // 10 It is a Equal operator

        System.out.println(a==b);   // False It is a Equal to operator

        System.out.println("==================");
        System.out.println(b==c);
        System.out.println("========");
        System.out.println(b!=c);

    }
}
