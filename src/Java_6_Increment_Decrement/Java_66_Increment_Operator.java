package Java_6_Increment_Decrement;

public class Java_66_Increment_Operator {
    public static void main(String[] args) {

        int a = 10 ;
        int b = ++a; // a value is increment so now a is 11
        System.out.println(a);
        System.out.println(b);

        // ERT
        // Line no / a / Result b
        //  6 / 10/ NA
        // 7/  ++a == 11  / 11
        // 8 / print 11 / 11
         // 9 / 11 / 11- print

    }
}
