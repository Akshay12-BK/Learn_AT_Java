package Java_6_Increment_Decrement;

public class Java_67_Post_ID {
    public static void main(String[] args) {
        int a = 10;
        System.out.println(++a);
        System.out.println(a);

        // Post ID  a++
        int b = 20;
        System.out.println(b++);
        System.out.println(b++);
        System.out.println(b);

    }

}
