package Java_6_Increment_Decrement;

public class Java_68_Pre_Post_Increment {
    public static void main(String[] args) {

        // Pre increment = ++a
        int a=10;
        System.out.println(++a);
        System.out.println(a);
        System.out.println(++a);
        System.out.println(a);

        // Post Increment = b++
        int b= 10;
        System.out.println(b++); //10
        System.out.println(b); // 11
        System.out.println(b++); // 11
        System.out.println(b); // 12


    }
}
