package Java_6_Increment_Decrement;

public class Java_69_Inc_Dec {
    public static void main(String[] args) {
        int a_Post = 10;
        System.out.println(a_Post++);  // 10

        System.out.println("============");
        System.out.println(a_Post++); // 11
        System.out.println("========");
        System.out.println(++a_Post);  // 12+1 = 13
    }
}
