package Loop;

public class Test_02_While {

    public static void main(String[] args) {
       // ICU
        int i = 3;    // Init
        while(i>0)     // condition
        {
            System.out.println(i);
            i--;   // updation
        }

        System.out.println("----------");
        System.out.println("----------");

        // ICU
        for (int j=0;j<10;j++){
            System.out.print(j);
        }
    }

}
